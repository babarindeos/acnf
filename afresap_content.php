    Objectives </H3></a>
  <p>
  	<span style="color:#900;font-style:italic;font-weight:bold;">AFRESAP</span> Capacity Building & Empowerment will provide:
    <ul>
       <li>Intensive training for ‘Qualified Beneficiaries’ in various aspects of hospitality development, management, businesses and skills.</li>
