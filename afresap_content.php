  <a name="Background"><H3 style="color:#069;">
    Background… </H3></a>
  <p>
 <strong>African Caribbean Network Foundation</strong> research on Africa’s socio-economic developmental programs established that Africa’s rapidly growing hospitality sector in the tourism industry is faced with surmounting infrastructural, management and human resource challenges to align with innovative global trends. The ‘Hospitality Sector’ is highly encumbered and constrained by lack of In-House Activities, Hospitality Skills, Capacity Building & Training Programs, Qualitative Human Resources, Improved Remunerations, innovative Products, Services, Industry and Professional Affiliations. Hence, ACNF implementation of this <span style="color:#900;font-style:italic;font-weight:bold;">‘AFRESAP</span> <span style="font-style:italic;color:#069;">Capacity Building & Empowerment’</span> as a pilot scheme of <strong>‘Afro-Caribbean Entrepreneurial & Skill Acquisition Program (AFRESAP)’.</strong> 
 </div>  




 
