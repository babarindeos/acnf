<a name="The-Program"><H3 style="color:#069;">
    The Program… </H3></a>
  <p>
  	<span style="color:#900;font-style:italic;font-weight:bold;">AFRESAP</span> Capacity Building & Empowerment is a ‘no-frills’ training and empowerment program innovatively designed and structured to qualitatively train, certify and empower qualified and unemployed Africans desirous of making a career in the hospitality sector of the tourism industry. The scheme is complemented with ‘Internship’ in reputable hospitality establishments, and does not require any ‘financial investment’ from beneficiaries. <span style="color:#900;font-style:italic;font-weight:bold;">AFRESAP</span> <span style="font-style:italic;color:#069;">Capacity Building & Empowerment</span> is established on the principles of Vision, Integrity, Opportunity, Partnership, Empowerment, Freedom and Success.</strong> 
