<h3>TINEX RECOGNITION AWARDS</h3></a>
<p><strong>TINEX Recognition Awards</strong> captioned<strong> “GEJAFE - Goodluck Ebele Jonathan Award For Excellence”</strong>, is based on President Goodluck Jonathan’s commitment towards the development and promotion of lasting relations and programs exchange between Nigeria and the Caribbean; and recognizes companies that have launched successful business, trade and investment relationships between Nigeria and the Caribbean. </p>
<p><strong>GEJAFE</strong> is designed to honour companies and individuals who have established either export or import trade links with the Caribbean, and those that have established a trade base in the Caribbean. It is supremely important for us to recognize the success of <strong>Nigeria-Caribbean Investors/Traders</strong> – they are <strong>‘Role Models’</strong> for all aspiring Nigerian and Caribbean people. The <strong>Award Winners </strong>will receive <strong>Plaques</strong> and <strong>Complementary Prizes</strong>. <br>
