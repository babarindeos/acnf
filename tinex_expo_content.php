<a name="overview"><h3 style="margin-bottom:-10px">OVERVIEW</h3></a>
<p>Historically and socio-culturally, Africa and the Caribbean are linked in many ways. In terms of racial, cultural, historical and situational similitude, the two divides also have so much in common. These affinities have potentials for a flourishing market for African products and other investments that will be of utmost benefit to Nigerian Manufacturers, Investors, Federal and State Governments. <br>
  <br>
In addition, the popularity of Nigerian cultural heritage, movies, 	music, and the ever increasing number of Nigerian Students and 	Citizens resident in the Caribbean has further enhanced interest 	and demand for Nigerian products. In spite of this popularity, 	interest and demand, 	the huge Caribbean market remains under-	exploited for the good of the people of both regions. <br>
<br>
<strong>“TRADE & INVEST NIGERIA EXPO (TINEX)”</strong> has been packaged by <strong>African Caribbean Network Foundation (ACNF)</strong> to effectively and optimally appropriate, the inherent trade and investment opportunities, bridge the gap between the two socio-culturally linked regions, and particularly in recognition of the huge market potentials for <strong>‘Made-in-Nigeria Products’</strong> in the <strong>Caribbean</strong>. <br>
<br>
TINEX is scheduled to take place on the twin Caribbean Island of 	St. Maarten (Dutch) and St. Martin (French) from Monday the 3rd 	through Saturday the 8th of March 2014. 
