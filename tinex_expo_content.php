<a name="impact"><h3>IMPACT</h3></a>
<ul style="margin-left:20px;">
<li><strong>TINEX</strong> will positively impact on the Federal Government of Nigeria’s economic diversification and transformation agenda. </li><br>
<li><strong>TINEX</strong> will enhance the building of bridges across the socio-economic divide between Nigeria and the Caribbean, and optimally improve Nigeria’s socio-economic image in the region. </li><br>
<li><strong>TINEX</strong> will optimally impact on the Federal Government of Nigeria’s initiatives and support of <strong>Nigerian Manufacturers, SMEs, Entrepreneurs</strong>, and the <strong>Entertainment (Nollywood) Industry</strong>. </li><br>
<li><strong>TINEX</strong> will create room for the development and promotion of <strong>new consumer markets, trade relations, joint venture partnerships, investment</strong> and <strong>export trade platforms</strong>, for <strong>Nigerian Manufacturers, SMEs</strong> and <strong>Businesses</strong>. </li><br>
<li><strong>TINEX</strong> will open the doors of Nigeria’s <strong>investment opportunities</strong> and <strong>trade potentials</strong> to Caribbean Businesses, Distributors, Merchants, and Investors, with the resultant effect of positive and lasting economic relations, between <strong>Nigeria</strong> and the <strong>Caribbean</strong>.</li> <br>
<li><strong>TINEX</strong> will provide for the introduction, distribution, integration, and profitable patronage of <strong>‘Made-in-Nigerian Products’</strong> in the <strong>Caribbean</strong>.  </li><br>
<li><strong>TINEX</strong> will enhance the Federal and State Governments initiatives towards the <strong>progressive diversification</strong> and <strong>revitalization</strong> of <strong>foreign direct revenue generation</strong> through the international promotion of <strong>‘Made-in-Nigeria Products’</strong>.</li> <br>
<li><strong>TINEX</strong> will positively improve on the <strong>‘international re-branding’</strong> of <strong>Nigeria’s</strong> image, using the highly patronized <strong>Nigerian (Nollywood) Movies</strong> and <strong>Music</strong> and other consumer products. </li><br>
<li>Improve the optimal patronage of <strong>Nigerian Entertainment Industry Products</strong>, while enhancing eradication of the <strong>‘incessant piracy’</strong> and <strong>‘flagrant abuse’</strong> of Nigeria’s <strong>‘intellectual properties’</strong> that is currently prevalent throughout the Caribbean.</li> <br>
<li>Create room for the development of <strong>‘artistic’</strong> and <strong>‘creative collaborations’</strong> in movie, music and  other areas of culture between Nigerian, Caribbean and Latin American <strong>‘Artistes’</strong> and <strong>‘Entertainment Industry’</strong>.</li>
</ul>
