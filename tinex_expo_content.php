<a name="coreobjectives"><h3>CORE OBJECTIVES</h3></a>
<strong>“TRADE & INVEST NIGERIA EXPO (TINEX)”</strong> ultimate goal is to promote and develop new vistas of <strong>business opportunities, foreign direct investments, trade relations, consumer markets</strong>, and improved <strong>socio-economic image</strong> for <strong>NIGERIA</strong> in the <strong>CARIBBEAN</strong>. 
