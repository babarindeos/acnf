<a name="expodirector"><h3>EXPO DIRECTOR</h3> </a>
The Executive Director of <strong>“TRADE & INVEST NIGERIA EXPO (TINEX)”</strong> is African Caribbean Network Foundation (ACNF) <strong>President General-in-Council: Ambassador Justin Duru</strong>, a Nigerian Theatre Artiste, Television Producer, Business Development Consultant, Project Manager, Human Resource and International Relations Professional.<br>
<strong>Ambassador Justin Duru</strong> is a creative, skilled and seasoned Theatre Artiste, Actor, Writer, Television Director & Producer, Radio Personality, Artistes Manager and Events Promoter, whose working professional experience includes Nigerian Television Authority (NTA), LBC-TV Channel 7, JustPearl Productions, Medialinks Communications, AfroCarib Communications, AfroCarib Impex, and AfroCarib Linx NV (All in St. Maarten); 3SCS TV Channel 7 Saba, AfroCarib Communications Barbados; Commission for Pan African Affairs (PANCOM) Barbados; Calypso Tours (West Africa) Limited; Carlin Concept International (Nig) Ltd, and CC-West (Nig) Ltd, among many others. <br>
<br>
<strong>Ambassador Justin Duru</strong> has served in numerous Commissions and Committees some of which include; Presidential Committee on Sao Tome & Principe Tourism Development - Nigeria, COJA All African Games 2003 Travel & Tour Committee - Nigeria, and Public Relations Officer - Men of Distinction Prayer Ministry, St. Marten/St. Martin. <br>
<br>
<strong>Ambassador Justin Duru</strong> has received many commendations among which include, <strong>“ACNF Special Envoy to Africa”</strong> from ACNF Barbados Chapter, and <strong>“Ambassador of Culture in the Diaspora”</strong> from the Island Government of St. Maarten (Netherlands Antilles). <br>
<br>
<strong>Ambassador Justin Duru</strong> as the President of ACNF International Governing Board is statutorily empowered with the responsibility of managing the Foundation’s <strong>Regional</strong> and <strong>Country Chapters</strong>. 
