  <p>
  	<span style="color:#900;font-style:italic;font-weight:bold;"></span> African Caribbean Network Foundation (ACNF) invests the <strong>'Afro-Caribbean Consolidated Mutual Fund'</strong> into Caribbean Equities, Impex, Hospitality, Mutual Funds, Real Estate and Tourism Projects, with resultant optimum returns to enhance the livelihoods of 'Low Income Earners' and the 'Less Privileged'. 
