    <td width="663" style="font-family:corbel;font-size:14px;padding:5px;border-right:1px solid #f1f1f1;"><p><span style="font-size:18px;font-weight:bold;color:#069"><a name="aboutacnf"></a>About African Caribbean Network Foundation (ACNF)</span></p>
    <p>Historically and Culturally, Africa and the Caribbean are linked in many ways. The two divides also have so much in common and perhaps much more than any other region of the world in terms of affinity, heritage, economic and political challenges. These shared history and situational similitude have however remained unexploited for the good of the governments and people of both regions. Recognizing the tremendous attractions and interests of the Caribbean Communities in their African heritage and links, anchored on the shared cultural heritage, historical values and racial affinities,<strong> ‘AFRICAN CARIBBEAN NETWORK FOUNDATION (ACNF)’</strong> was established in the Caribbean Island of<strong> ‘St. Maarten’</strong> in 1996 to redress a further drifting apart of the two culturally linked regions, and build bridges over this ever expanding fissure.     
<p><br>
