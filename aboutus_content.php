<a name="foundation"></a>
<span style="font-size:18px;font-weight:bold;color:#069">The Foundation</span>
    <p><strong>African Caribbean Network Foundation (ACNF)</strong> was established as a non-governmental  organization, to develop and promote bilateral and multilateral relations and exchange  programs between Africa and the Caribbean. ACNF maintains dynamic affiliations  and excellent working relationships with numerous governments, institutions and  organizations; and boasts of endorsements and commendations from African and  Caribbean Governments, Leaders, Presidents, Prime Ministers, Net-Worth  Personalities, Institutions, Policy and Decision Makers of repute. Ultimately,  integrity and respect for African and Caribbean socio-cultural ethos, affinity  and shared heritage, guide everything <strong>ACNF</strong> do to attain the target objective of <strong>&lsquo;</strong><strong><em>Bridging The African-Caribbean Gap&rsquo;</em>.</strong></p>
<p>&nbsp;</p>
<hr size="1px" color='#e1e1e1'>
  <p><span style="font-size:18px;font-weight:bold;color:#069"><br>
      <a name="vision"></a>Vision</span>
<p>To develop and promote positive relations and programs exchange between Africa and the Caribbean, aimed at <strong>“Bridging The African Caribbean Gap”</strong> in the widest sense of the word. </p>
<br>
<hr size="1px" color='#e1e1e1'>
