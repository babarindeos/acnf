    <a name="Afresap"></a>
    <p><span style="font-family:'Trebuchet MS', Arial, Helvetica, sans-serif;font-size:25px;color:#069;font-weight:bold;">AFRESAP</span><br>
    <p><img src="images/africa_map.png" alt="Afresap" align="left">
      <span style="font-family:'Trebuchet MS', Arial, Helvetica, sans-serif;font-size:20px;color:#E36C0A;font-weight:bold;">Afro-Caribbean Entrepreneurial & Skills Acquisition Program 
</span><br><br><br>
   </p>
<p>
<strong>AFRESAP</strong> is a <strong>‘Poverty Alleviation Program’</strong> being established by ACNF to create, develop, facilitate, provide and promote <strong>Entrepreneurial, Employment, Investment, Skills Acquisition and Socio-Economic Development Programs</strong> for Youths in Africa and the Caribbean. 
</p>
<p>

AFRESAP promotes, facilitates and guarantees <strong>Financial Grants, Technical Assistance, and Training</strong> for unemployed and qualified African and Caribbean Youths through a broad range of ‘Entrepreneurial’ and ‘Skill Acquisition Projects’ to foster Youth Empowerment, geared towards sustainable socio-economic developments, aided by renowned ‘Financial Institutions - Counterpart Funding Capital, Monitoring and Initiative’. 
</p>
<p>
<strong>AFRESAP</strong> is designed to serve as a model in the creation, establishment, funding and promotion of Small & Medium Enterprise Businesses and Jobs for unemployed African and Caribbean Youths incorporating the concept of upscale marketing, innovative technology-driven trends, pioneering elastic and dynamic entrepreneurship projects, and skill acquisition programs for all the beneficiaries. 


</p>
